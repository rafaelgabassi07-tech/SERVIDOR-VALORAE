export function normalizePeerText(raw = '') {
  return String(raw || '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/&/g, ' e ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

export function normalizePeerGroup(raw = '') {
  const key = normalizePeerText(raw);
  if (!key) return '';
  if (/recebiveis|cri|fii-papel|fundo-papel/.test(key)) return 'fii-recebiveis';
  if (/fii-logistica|logistica|galpao/.test(key)) return 'fii-logistica';
  if (/shopping|mall/.test(key)) return 'fii-shopping';
  if (/renda-urbana|urbana/.test(key)) return 'fii-renda-urbana';
  if (/hibrido/.test(key)) return 'fii-hibrido';
  if (/seguro|seguradora/.test(key)) return 'seguros';
  if (/bolsa|infraestrutura-de-mercado/.test(key)) return 'infraestrutura-mercado';
  if (/banco|financeiro/.test(key)) return 'bancos';
  if (/petroleo|oleo|gas|biocombust/.test(key)) return 'petroleo-gas';
  if (/mineracao/.test(key)) return 'mineracao';
  if (/siderurgia/.test(key)) return 'siderurgia';
  if (/papel|celulose/.test(key)) return 'papel-celulose';
  if (/energia-eletrica|eletrica|utilidade-publica/.test(key)) return 'energia-eletrica';
  if (/saneamento/.test(key)) return 'saneamento';
  if (/etf-brasil|ibovespa|small|dividendos/.test(key)) return 'etf-brasil';
  if (/exterior|sp-500|s-p-500|nasdaq/.test(key)) return 'etf-exterior';
  if (/tecnologia|software|semicondutor|internet/.test(key)) return 'bdr-tecnologia';
  return key;
}

export function getCatalogAssetPeerProfile(rawTicker = '') {
  const ticker = String(rawTicker || '').trim().toUpperCase();
  return ASSET_SUGGESTION_CATALOG.find(item => item.ticker === ticker) || null;
}

export function describePeerCompatibility(rawBaseTicker = '', rawCompareTicker = '') {
  const base = getCatalogAssetPeerProfile(rawBaseTicker);
  const target = getCatalogAssetPeerProfile(rawCompareTicker);
  if (!rawBaseTicker || !rawCompareTicker) {
    return null;
  }
  if (!base || !target) {
    return {
      comparable: null,
      samePeerGroup: null,
      baseKnown: Boolean(base),
      targetKnown: Boolean(target),
      base: base ? { ticker: base.ticker, sector: base.sector, segment: base.segment, peerGroup: base.peerGroup } : null,
      target: target ? { ticker: target.ticker, sector: target.sector, segment: target.segment, peerGroup: target.peerGroup } : null,
      message: 'Não há metadados setoriais suficientes para confirmar a compatibilidade do par.',
    };
  }
  const samePeerGroup = normalizePeerGroup(base.peerGroup) === normalizePeerGroup(target.peerGroup);
  const sameSector = normalizePeerText(base.sector) === normalizePeerText(target.sector);
  return {
    comparable: samePeerGroup,
    samePeerGroup,
    sameSector,
    confidence: samePeerGroup ? 'HIGH' : sameSector ? 'LOW_SECTOR_ONLY' : 'LOW_DIFFERENT_GROUP',
    comparisonMode: samePeerGroup ? 'decision' : 'informative',
    baseKnown: true,
    targetKnown: true,
    base: { ticker: base.ticker, sector: base.sector, segment: base.segment, peerGroup: base.peerGroup },
    target: { ticker: target.ticker, sector: target.sector, segment: target.segment, peerGroup: target.peerGroup },
    message: samePeerGroup
      ? 'Par confirmado no mesmo grupo comparável.'
      : sameSector
        ? 'Mesmo setor amplo, mas grupo/segmento diferente; comparação informativa recomendada.'
        : 'Par pertence a grupos setoriais diferentes; comparação manual permitida, mas sugestões automáticas continuam restritas ao grupo do ativo-base.',
  };
}

export function buildPeerCatalogEntries(rawPeerOf = '', options = {}) {
  const baseTicker = String(rawPeerOf || '').trim().toUpperCase();
  const base = getCatalogAssetPeerProfile(baseTicker);
  if (!base) return { base: null, peers: [] };
  const queryKey = normalizePeerText(options.query || '');
  const max = Math.max(1, Math.min(Number(options.max || 12) || 12, 25));
  const includeBase = options.includeBase === true;
  const peers = ASSET_SUGGESTION_CATALOG
    .filter(item => item.peerGroup === base.peerGroup)
    .filter(item => includeBase || item.ticker !== base.ticker)
    .filter(item => {
      if (!queryKey) return true;
      return [item.ticker, item.name, item.segment, item.sector].some(value => normalizePeerText(value).includes(queryKey));
    })
    .sort((a, b) => a.ticker.localeCompare(b.ticker))
    .slice(0, max);
  return { base, peers };
}

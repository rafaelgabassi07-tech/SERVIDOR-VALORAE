process.env.VALORAE_DISABLE_EXTERNAL = '1';
import { buildMobilePortfolioSync } from '../lib/contracts/mobile.js';
import { buildAssetsPayload, buildMarketMovers } from '../lib/sources/quotes.js';
import { getNews } from '../lib/sources/news.js';
import { getIpcaSeries } from '../lib/sources/ipca.js';
import { RELEASE } from '../lib/core/release.js';

if (RELEASE.version !== '21.13.3') throw new Error(`Versão inesperada: ${RELEASE.version}`);
const sample = [{ ticker: 'PETR4', quantity: 100, avgPrice: 30, currentPrice: 32, firstPurchaseDate: '2024-01-02' }];
const mobile = await buildMobilePortfolioSync({ positions: sample, dividendPositions: sample, includeAnalysis: true, includeHistory: true, includeIpca: true, includeDividends: false, includeRankings: false, months: 6 });
if (mobile.endpoint !== 'mobile-portfolio-sync') throw new Error('Contrato mobile inválido.');
if (!Array.isArray(mobile.ipca?.points) || mobile.ipca.points.length < 1) throw new Error('IPCA não retornou série utilizável.');
const assets = await buildAssetsPayload({ tickers: ['PETR4','VALE3'], timeoutMs: 20 });
if (!Array.isArray(assets.assets) || assets.assets.length !== 2) throw new Error('Assets/cotações não retornaram contrato em lote.');
const news = await getNews({ timeoutMs: 20, limit: 2 });
if (!Array.isArray(news.items) || news.items.length < 1) throw new Error('Notícias não retornaram contrato utilizável.');
const rankings = await buildMarketMovers({ timeoutMs: 20, limit: 3 });
if (!Array.isArray(rankings.highs) || rankings.highs.length < 3 || !Array.isArray(rankings.lows) || rankings.lows.length < 3) throw new Error('Rankings não retornaram altas/baixas utilizáveis.');
const ipca = await getIpcaSeries(6);
if (!Array.isArray(ipca.points) || ipca.points.length !== 6) throw new Error('IPCA direto inválido.');
console.log('VALORAE Proxy runtime feeds v21.13.3 OK');

import assert from 'node:assert/strict';
import fs from 'node:fs';
const pkg = JSON.parse(fs.readFileSync('package.json','utf8'));
const meta = JSON.parse(fs.readFileSync('metadata.json','utf8'));
const release = fs.readFileSync('lib/release/current.js','utf8');
const route = fs.readFileSync('routes/portfolio/insights-bundle.js','utf8');
const sw = fs.readFileSync('public/service-worker.js','utf8');
const forbidden = [['Ae','ro','Scrape'],['Ae','ro','Scraper'],['Ves','to'],['ae','roscrape'],['ves','to']].map(parts => parts.join(''));
assert.ok(release.includes("VALORAE_PUBLIC_VERSION = '21.12.93'"), 'release pública 21.12.93');
assert.equal(pkg.valorae.releasePatch, '21.12.93-valorae-native-contract-polish');
assert.equal(meta.releasePatch, '21.12.93-valorae-native-contract-polish');
assert.equal(meta.patch, '21.12.93-valorae-native-contract-polish');
assert.ok(route.includes("const INSIGHTS_BUNDLE_VERSION = '21.12.93'"), 'bundle version 21.12.93');
assert.ok(route.includes("style: 'valorae-single-request-cache-first'"), 'estilo nativo VALORAE explícito');
assert.ok(route.includes('const includeRankings = explicitInclude'), 'flags opcionais respeitadas');
assert.ok(route.includes('const signature = JSON.stringify'), 'coalescing por assinatura estável');
assert.ok(sw.includes('valorae-proxy-server-v21-12-93'), 'service worker cache v21.12.93');
const central = [JSON.stringify(pkg), JSON.stringify(meta), release, route, sw].join('\n');
for (const token of forbidden) assert.equal(central.includes(token), false, `referência externa proibida: ${token}`);
console.log('VALORAE Proxy native contract v21.12.93 OK');
